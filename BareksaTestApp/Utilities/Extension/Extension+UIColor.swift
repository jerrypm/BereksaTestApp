    static let lineIndicatorColor = UIColor(named: SC.lineIndicatorColor.rawValue)!
    static let buttonGreenColor = UIColor(named: SC.buttonColor.rawValue)!
    static let textPrimary = UIColor(named: SC.textPrimary.rawValue)!
    static let purple = UIColor(named: SC.purple.rawValue)!
}
